    <h1>The Fair Credit Reporting Act</h1>
    <h2>602.  Congressional Findings and statement of purpose [15 U.S.C 1681] <br />(a) Accuracy and fairness of credit reporting.  The Congress makes the following findings:</h2>
    <div class="hr">&nbsp;</div>
    <p><strong>(1)</strong> The banking system is dependent upon fair and accurate credit reporting.  Inaccurate credit reports directly impair the efficiency of the banking system, and unfair credit reporting methods undermine the public confidence which is essential to the continued functioning of the banking system.</p><p><strong>(2)</strong> An elaborate mechanism has been developed for investigating and evaluating the credit worthiness, credit standing, credit capacity, character, and general reputation of consumers.</p><p><strong>(3)</strong> Consumer reporting agencies have assumed a vital role in assembling and evaluating consumer credit and other information on consumers.</p><p><strong>(4)</strong> There is a need to insure that consumer reporting agencies exercise their grave responsibilities with fairness, impartiality, and a respect for the consumer's right to privacy.</p>
    
    <h2>(b) Reasonable procedures.</h2>  <p>It is the purpose of this title to require that consumer reporting agencies adopt reasonable procedures for meeting the needs of commerce for consumer credit, personnel, insurance, and other information in a manner which is fair and equitable to the consumer, with regard to the confidentiality, accuracy, relevancy, and proper utilization of such information in accordance with the requirements of this title.</p>
    <a href="http://www.ftc.gove/os/statutes/031224fcra.pdf" target="_blank">http://www.ftc.gove/os/statutes/031224fcra.pdf</a><br />
   
  <h1>Fair Debt Collection Practices Act</h1>
  <h2>802. Congressional findings and declaration of purpose</h2>
  <p><strong>(a)</strong>There is abundant evidence of the use of abusive, deceptive, and unfair debt collection practices by many debt collectors.  Abusive debt collection practices contribute to the number of personal bankruptcies, to marital instability, to the loss of jobs, and to invasions of individual privacy.</p>
  <p><strong>(b)</strong>Existing laws and procedures for redressing these injuries are inadequate to protect consumers.</p>
  <p><strong>(c)</strong>Means other than misrepresentation or other abusive debt collection practices are available for the effective collection of debts.</p>
  <p><strong>(d)</strong>Abusive debt collection practices are carried on to a substantial extent in interstate commerce and through means and instrumentalities of such commerce.  Even where abusive debt collection practices are purely intrastate in character, they nevertheless directly affect interstate commerce.</p>
  <p><strong>(e)</strong>It is the purpose of this title to eliminate abusive debt collection practices by debt collectors, to insure that those debt collectors who refrain from using abusive debt collection practices are not competitively disadvantaged, and to promote consistent State action to protect consumers against debt collection abuses.</p>
